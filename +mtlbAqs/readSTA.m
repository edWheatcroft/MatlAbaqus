%% Added by Ed
%  - Removes the last two rows of the table if they're NaN
%  - Splits the table into its steps. These sub-tables are output as the fields of outStruct
%  - Adds a specific logical variable for un-converged attempts
%  - May add other functionality later to de-textify the attempts and riks flag...

% remove last two rows if they're nan. Take this odd iterative approach in case we read the file before both rows have been written by ABAQUS
