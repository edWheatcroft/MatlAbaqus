%
% Inputs:
%   fileName  - char or string: name of the .sta file (extension optional)
%   fileDir   - char or string: directory containing the file
%
% Outputs:
%   staTable  - table containing the parsed .sta records (one row per record)
%   outStruct - struct with fields "step_<n>" where each field is a subtable
%               for the corresponding analysis step
%
% Example:
%   [tbl, byStep] = readSTA('job1','C:\myJobs')
%
% Notes:
%   - Parser expects a standard ABAQUS .sta layout (startRow = 6 in this file).
%   - This function supplements the auto-generated import code and adds
%     post-processing such as removal of trailing NaN rows and an
%     "unconvMask" flag for unconverged attempts.

