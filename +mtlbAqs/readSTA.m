staTable = addvars(staTable, unconvMask, 'After', 'attempt');

% sort into a struct where each field is the .sta table for a single analysis step.
steps = unique(staTable.step);
numSteps = length(steps);
for i = 1:numSteps
    currStepMask = staTable.step == steps(i);
    currStepTab = staTable(currStepMask,:);
    currStepName = ['step_', num2str(steps(i))];
    outStruct.(currStepName) = currStepTab;
end


end




